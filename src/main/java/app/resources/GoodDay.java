package app.resources;
/**
 * This represents the class for the HelloThere task.
 */
public class GoodDay {

  /**
   * This represents the class for greeting the user.
   *
   * @param name - The name of the person to greet
   * @return - The greeting
   */
  public String goodDay(String name) {
    return "Good Day, " + name + ". I hope you have a great day!";
  }   
}